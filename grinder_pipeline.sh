#!/bin/bash
# grinder_pipeline.sh v1.2
# Ring-0 Sovereign Data Grinder - Kali Linux master orchestration harness
# Supports --airgap, --parallel (ProcessPoolExecutor + xargs orchestration)
# Strict flock(1) locking on Master_Ring0.md append to prevent races under heavy concurrency
# + GPG provenance

set -euo pipefail

AIRGAP=false
PARALLEL=false
TARGET_DIR=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --airgap|--local|-a)
      AIRGAP=true
      shift
      ;;
    --parallel|-p)
      PARALLEL=true
      shift
      ;;
    --help|-h)
      echo "Usage: $0 [--airgap] [--parallel] [target_directory]"
      echo "  --airgap    Use airgapped_sanitizer.py (local Ollama)"
      echo "  --parallel  Enable concurrent extraction/sanitization with flock-protected append"
      exit 0
      ;;
    *)
      if [ -z "$TARGET_DIR" ]; then
        TARGET_DIR="$1"
      fi
      shift
      ;;
  esac
done

TARGET_DIR="${TARGET_DIR:-./raw_data}"
OUTPUT_MD="Master_Ring0.md"
EXTRACTOR="./ring0_extractor.py"

if $AIRGAP; then
  SANITIZER="./airgapped_sanitizer.py"
  MODE="AIRGAPPED (local Ollama)"
else
  SANITIZER="./api_sanitizer.py"
  MODE="EXTERNAL API"
fi

if $PARALLEL; then
  MODE+=" + PARALLEL (ProcessPool + flock)"
fi

echo "[Ring-0] Initializing Sovereign Data Refinery Pipeline (v1.2)" >&2
echo "[Ring-0] Mode: $MODE" >&2
echo "[Ring-0] Target directory: $TARGET_DIR" >&2
echo "[Ring-0] Master output: $OUTPUT_MD" >&2

if [ ! -d "$TARGET_DIR" ]; then
    echo "[Ring-0] ERROR: Target directory does not exist: $TARGET_DIR" >&2
    exit 1
fi

chmod +x "$EXTRACTOR" "$SANITIZER" 2>/dev/null || true

{
    echo "# Ring-0 Master Data Corpus - $(date -Iseconds)"
    echo ""
    echo "**Generated by:** Khawrizm-Data-Refinery v1.2 | Sulaiman Alshammari | KHAWRIZM Forensic Labs"
    echo "**Architecture:** Ring-0 Master Synthesizer | Mode: $MODE"
    echo "**Classification:** Sovereign / Zero-Telemetry / Air-Gapped Extraction Phase"
    echo ""
    echo "---"
    echo ""
} > "$OUTPUT_MD"

file_count=0
processed_count=0
LOCKFILE="${OUTPUT_MD}.lock"

touch "$LOCKFILE"

process_one() {
    local file="$1"
    local extracted sanitized
    extracted="$(python3 "$EXTRACTOR" "$file" 2>/dev/null || echo "")"
    if [ -z "$extracted" ] || [[ "$extracted" == *"EXTRACTION ERROR"* ]]; then
        return
    fi
    sanitized="$(echo "$extracted" | python3 "$SANITIZER" - 2>/dev/null || echo "")"
    if [ -n "$sanitized" ] && [[ "$sanitized" != *"No content"* ]]; then
        (
            flock -x 200
            {
                echo "$sanitized"
                echo ""
                echo "---"
                echo ""
            } >> "$OUTPUT_MD"
        ) 200>"$LOCKFILE"
        echo "[Ring-0] Appended: $file" >&2
        return 0
    fi
    return 1
}

export -f process_one

export EXTRACTOR SANITIZER OUTPUT_MD LOCKFILE

if $PARALLEL; then
    # Parallel orchestration: leverage ring0_extractor.py ProcessPoolExecutor when dir, or xargs jobs
    # Flock serializes appends to guarantee deterministic MD order
    echo "[Ring-0] Starting parallel workload..." >&2
    find "$TARGET_DIR" -type f \( ! -name "$(basename "$OUTPUT_MD")" -a ! -name "*.py" -a ! -name "*.sh" -a ! -name ".*" \) -print0 | \
        xargs -0 -P "$(nproc)" -I {} bash -c 'process_one "$@"' _ {} || true
else
    # Sequential with flock protection (safe for concurrent sh instances)
    while IFS= read -r -d $'\0' file; do
        ((file_count++))
        echo "[Ring-0] Processing file $file_count: $file" >&2
        if process_one "$file"; then
            ((processed_count++))
        fi
    done < <(find "$TARGET_DIR" -type f \( ! -name "$(basename "$OUTPUT_MD")" -a ! -name "*.py" -a ! -name "*.sh" -a ! -name ".*" \) -print0)
fi

echo "" >&2
echo "[Ring-0] Pipeline complete." >&2
echo "[Ring-0] Files scanned: $file_count | Sections written: $processed_count" >&2
echo "[Ring-0] Master_Ring0.md size: $(wc -c < "$OUTPUT_MD") bytes" >&2

# Cryptographic Supply Chain Integrity
if command -v gpg >/dev/null 2>&1; then
    if gpg --batch --yes --detach-sign --armor --output "${OUTPUT_MD}.sig" "$OUTPUT_MD" 2>/dev/null; then
        echo "[Ring-0] GPG signature created: ${OUTPUT_MD}.sig" >&2
    else
        echo "[Ring-0] GPG signing skipped" >&2
    fi
else
    echo "[Ring-0] WARNING: gpg not found" >&2
fi

echo "[Ring-0] Status: READY FOR NOTEBOOKLM + KERNEL ENFORCED AIRGAP + PARALLEL + VERIFIABLE PROVENANCE" >&2
rm -f "$LOCKFILE" 2>/dev/null || true
